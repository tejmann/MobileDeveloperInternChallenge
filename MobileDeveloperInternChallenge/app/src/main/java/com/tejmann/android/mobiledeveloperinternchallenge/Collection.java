package com.tejmann.android.mobiledeveloperinternchallenge;

public class Collection {
    private String title;
    private String id;

    public Collection(String title, String id) {
        this.title = title;
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public String getId() {
        return id;
    }
}
